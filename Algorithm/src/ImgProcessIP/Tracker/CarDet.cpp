HRESULT CCarDetector::Init()
{
    m_fInit = false;

    bool fLoad = true;   // 已经加载过
    if (m_pDetCtrl == NULL)
    {
        m_pDetCtrl = svIPDetApi::CreateDetCtrl();
        fLoad = false;
    }
    
    if(!m_pDetCtrl)
    {
        return E_OUTOFMEMORY;
    }

    HRESULT hr = S_OK;

    svIPDetApi::CTRL_PARAM cCtrlParam;

    //TODO: 设置合适的环境统计阈值
    cCtrlParam.nNightTH = 60; 
    cCtrlParam.nEnvLightMaxCheckFrameCount = 300;
    cCtrlParam.nEnvLightSwitchFramesTH = 256;

    // init
    hr = Svresult2Hresult(m_pDetCtrl->Init(&cCtrlParam));
    if(hr != S_OK)	return hr;

    if (!fLoad)
    {
        hr = Svresult2Hresult(m_pDetCtrl->LoadModel(DAY_MODEL, 0, g_rgModel0_day, sizeof(g_rgModel0_day)));
        if(hr != S_OK)	return hr;
        hr = Svresult2Hresult(m_pDetCtrl->LoadModel(DAY_MODEL, 1, g_rgModel1_day, sizeof(g_rgModel1_day)));
        if(hr != S_OK)	return hr;
        hr = Svresult2Hresult(m_pDetCtrl->LoadModel(DAY_MODEL, 2, g_rgModel2_day, sizeof(g_rgModel2_day)));
        if(hr != S_OK)	return hr;

        hr = Svresult2Hresult(m_pDetCtrl->LoadModel(NIGHT_MODEL, 3, g_rgModel0_night, sizeof(g_rgModel0_night)));
        if(hr != S_OK)	return hr;
        hr = Svresult2Hresult(m_pDetCtrl->LoadModel(NIGHT_MODEL, 4, g_rgModel1_night, sizeof(g_rgModel1_night)));
        if(hr != S_OK)	return hr;
        hr = Svresult2Hresult(m_pDetCtrl->LoadModel(NIGHT_MODEL, 5, g_rgModel2_night, sizeof(g_rgModel2_night)));
        if(hr != S_OK)	return hr;

#ifdef USE_L2_MODEL
        hr = Svresult2Hresult(m_pDetCtrl->LoadL2Model(DAY_MODEL, g_rgL2Model_day, sizeof(g_rgL2Model_day)));
        if(hr != S_OK)	return hr;
        hr = Svresult2Hresult(m_pDetCtrl->LoadL2Model(NIGHT_MODEL, g_rgL2Model_night, sizeof(g_rgL2Model_night)));
        if(hr != S_OK)	return hr;
#endif
    }

    //sv::utSetTraceCallBack_TXT(OutPutMsg);

    m_fInit = true;

	return hr;
}

