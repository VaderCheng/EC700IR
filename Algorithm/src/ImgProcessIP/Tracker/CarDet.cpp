static HRESULT Svresult2Hresult(sv::SV_RESULT svrt)
{
    HRESULT hr;
    switch(svrt)
    {
    case sv::RS_S_OK:
        hr = S_OK;
        break;
    case sv::RS_S_FALSE:
        hr = S_FALSE;
        break;
    case sv::RS_E_FAIL:
        hr = E_FAIL;
        break;
    case sv::RS_E_OUTOFMEMORY:
        hr = E_OUTOFMEMORY;
        break;
    case sv::RS_E_INVALIDARG:
        hr = E_INVALIDARG;
        break;
    case sv::RS_E_UNEXPECTED:
        hr = E_UNEXPECTED;
        break;
    case sv::RS_E_OBJNOINIT:
        hr = E_OBJ_NO_INIT;
        break;
    case sv::RS_E_NOTIMPL:
        hr = E_NOTIMPL;
        break;
    default :
        hr = E_UNEXPECTED;
        break;
    }

    return hr;
}

